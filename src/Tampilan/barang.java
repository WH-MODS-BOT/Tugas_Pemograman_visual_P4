    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed

        String sql = "INSERT INTO barang (kd_brg, nm_brg, jenis, hargabeli, hargajual) VALUES (?,?,?,?,?)";

        try{
            String id = generateID();

            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, id);
            stat.setString(2, txtnm.getText());
            stat.setString(3, cmbxBrg.getSelectedItem().toString());
            stat.setString(4, harga_beli.getText());
            stat.setString(5, harga_jual.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
            kosong();

        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e);
        }

        datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed

        if (id_pilih == null) {
            JOptionPane.showMessageDialog(null, "Pilih data dulu!");
            return;
        }

        try{
            String sql = "UPDATE barang SET nm_brg=?, jenis=?, hargabeli=?, hargajual=? WHERE kd_brg=?";
            PreparedStatement stat = conn.prepareStatement(sql);

            stat.setString(1, txtnm.getText());
            stat.setString(2, cmbxBrg.getSelectedItem().toString());
            stat.setString(3, harga_beli.getText());
            stat.setString(4, harga_jual.getText());
            stat.setString(5, id_pilih);

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah");

            kosong();
            id_pilih = null;

        } catch (SQLException e){
            JOptionPane.showMessageDialog(null, "Data gagal diubah: " + e);
        }

        datatable();
    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed

        int ok = JOptionPane.showConfirmDialog(null, "Hapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (ok == JOptionPane.YES_OPTION){
            try{
                String sql = "DELETE FROM barang WHERE kd_brg=?";
                PreparedStatement stat = conn.prepareStatement(sql);
                stat.setString(1, id_pilih);
                stat.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data berhasil dihapus");
                kosong();
                id_pilih = null;

            } catch (SQLException e){
                JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + e);
            }
            datatable();
        }
    }//GEN-LAST:event_bhapusActionPerformed

