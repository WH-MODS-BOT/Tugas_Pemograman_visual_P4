protected void kosong(){
    txtnm.setText("");
    harga_beli.setText("");
    harga_jual.setText("");
    txtcari.setText("");
    cmbxBrg.setSelectedIndex(0);
}

protected void aktif(){
    txtnm.requestFocus();
}
protected void datatable(){
    Object[] Baris = {"Kode Barang", "Nama", "Jenis", "Harga Beli", "Harga Jual"};
    tabmode = new DefaultTableModel(null, Baris);
    String cariitem = txtcari.getText();

    try {
        String sql = "SELECT * FROM barang WHERE kd_brg LIKE '%"+cariitem+"%' OR nm_brg LIKE '%"+cariitem+"%' ORDER BY kd_brg ASC";
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);

        while (hasil.next()){
            tabmode.addRow(new Object[]{
                hasil.getString("kd_brg"),
                hasil.getString("nm_brg"),
                hasil.getString("jenis"),
                hasil.getString("hargabeli"),
                hasil.getString("hargajual")
            });
        }

        tblplgn.setModel(tabmode);

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal dipanggil "+e);
    }
}
    private String generateID() {
    String kd = "BR0001";

    try {
        String sql = "SELECT kd_brg FROM barang ORDER BY kd_brg DESC LIMIT 1";
        Statement stat = conn.createStatement();
        ResultSet rs = stat.executeQuery(sql);

        if (rs.next()) {
            String last = rs.getString("kd_brg");
            int num = Integer.parseInt(last.substring(2));
            num++;
            kd = String.format("BR%04d", num);
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal generate ID: " + e);
    }

    return kd;
}
