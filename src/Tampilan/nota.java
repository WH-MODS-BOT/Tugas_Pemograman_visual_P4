  public void hitung() {
    int total = 0;
    for (int i = 0; i < this.tbltransaksi.getRowCount(); i++) {
    int amount = Integer.valueOf(this.tbltransaksi.getValueAt(i, 5).toString()).intValue();
    total += amount;
    } 
    this.txtttotal.setText(Integer.toString(total));
  }
