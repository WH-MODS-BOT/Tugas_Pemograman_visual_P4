    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        String fd = sdf.format(this.jtgl.getValue());
        String sql = "INSERT INTO nota(id_nota,tgl_nota,id_plgn,id_kasir) VALUES (?,?,?,?)";
        String zsql = "INSERT INTO isi(id_nota,kd_brg,hb,hj,qty) VALUES (?,?,?,?,?)";
    try {
        PreparedStatement stat = this.conn.prepareStatement(sql);
        stat.setString(1, this.txtidnota.getText());
        stat.setString(2, fd);
        stat.setString(3, this.txtid.getText());
        stat.setString(4, this.id_kasir.getText());
        stat.executeUpdate();
        int t = this.tbltransaksi.getRowCount();
        for (int i = 0; i < t; i++) {
        String xkd = this.tbltransaksi.getValueAt(i, 0).toString();
        String xhb = this.tbltransaksi.getValueAt(i, 2).toString();
        String xhj = this.tbltransaksi.getValueAt(i, 3).toString();
        String xqty = this.tbltransaksi.getValueAt(i, 4).toString();
        PreparedStatement stat2 = this.conn.prepareStatement(zsql);
        stat2.setString(1, this.txtidnota.getText());
        stat2.setString(2, xkd);
        stat2.setString(3, xhb);
        stat2.setString(4, xhj);
        stat2.setString(5, xqty);      
        stat2.executeUpdate();
      } 
        JOptionPane.showMessageDialog(null, "data berhasil disimpan");
    }
        catch (SQLException e) {
        JOptionPane.showMessageDialog(null, "data gagal disimpan" + e);
    } 
        kosong();
        this.tabmode.setRowCount(0);
        this.txtttotal.setText("");
        aktif();
        autonumber();
    }//GEN-LAST:event_bsimpanActionPerformed

